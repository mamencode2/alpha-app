function extractEnglishWords(text) {
  // Create an empty array to store the English words.
  let englishWords = [];

  // Split the text into words.
  const words = text.split(" ");

  // Iterate over the words and check if they are all alphabetic characters.
  for (const word of words) {
    if (word.match(/^[a-zA-Z]+$/)) {
      englishWords.push(word);
    }
  }

  // Return the array of English words.
  return englishWords;
}

let extrDic = dicData.map((el) => {
  let engC = extractEnglishWords(el.word);
  if (engC.length === 1) {
    engC = engC[0];
  } else {
    let chr = `${engC.map((itm) => itm)}`;
    chr = chr.split(",").join(" ");
    engC = chr;
  }
  return {
    ...el,
    engC
  };
});

let amharicRep = extrDic.map((item) => {
  let { word, engC } = item;
  let amharicWord = word.replace(`${engC}`, "").trim();
  return {
    ...item,
    amharicWord
  };
});
export { dicData, extrDic, amharicRep };
